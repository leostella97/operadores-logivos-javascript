// %% representa AND (e)
// || representa OR (ou)
// ! representa NOT (não)

let num1 = 10
let num2 = 2

// AND
if (num1 == 10 && num2 == 2){
    console.log ("num1 é igual a " + num1 + " e num2 é igual a " + num2)
}

// OR
if (num1 == 10 || num2 == 2){
    console.log ("num1 é igual a " + num1 + " ou num2 é igual a " + num2)
}

// NOT
if (!(num1 == 1)){
    console.log ("num1 não é igual a " + num1)
}

